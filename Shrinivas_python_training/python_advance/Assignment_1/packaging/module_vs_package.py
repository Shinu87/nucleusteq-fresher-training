"""
Module:
A module is a single Python file (.py) that contains code such as functions, classes, or variables.

Example:

calculator.py

def add(a, b):
    return a + b

Here, calculator.py is a module because it is a single Python file.
"""

"""
Package:
A package is a folder that contains multiple related Python modules.
It usually contains an __init__.py file.

Example:

The package name is math_operations.

Files inside the package:
- __init__.py
- addition.py
- subtraction.py
- multiplication.py


Here:
- addition.py is a module
- subtraction.py is a module
- multiplication.py is a module
- math_operations is a package

""" 

"""
Difference Between Module and Package:

1. A module is a single Python file.
2. A package is a folder that contains multiple modules.
3. Modules are used to organize code in one file.
4. Packages are used to organize related modules together.
5. Packages make large projects easier to manage.


A module is a single Python file, while a package is a collection of related modules stored inside a folder.
"""