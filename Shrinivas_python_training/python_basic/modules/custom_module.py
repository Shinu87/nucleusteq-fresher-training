"""
Custom module containing utility functions.
"""


def find_cube(number: int) -> int:
    """
    Returns the cube of a number.

    Time Complexity: O(1)
    Space Complexity: O(1)
    """

    return number ** 3


def find_area_of_square(side: float) -> float:
    """
    Returns the area of a square.

    Time Complexity: O(1)
    Space Complexity: O(1)
    """

    return side * side